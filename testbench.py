def _gen_intervals(rng):
    k = rng.randint(0, 5)
    return ([[(a := rng.randint(0, 10)), a + rng.randint(0, 5)] for _ in range(k)],)

def _gen_brackets(rng):
    return ("".join(rng.choice("()[]{}") for _ in range(rng.randint(0, 8))),)

def _gen_twosum(rng):
    nums = [rng.randint(0, 9) for _ in range(rng.randint(2, 6))]
    return (nums, rng.randint(0, 18))

def _gen_rle(rng):
    return ("".join(rng.choice("abc") for _ in range(rng.randint(0, 8))),)


