"""TestBench-Forge — an RL gym that trains agents to write the test suite that catches
the most bugs.

The agent is shown a module + its reference implementation and must write a pytest-style
test suite (functions named test_*). Reward = fraction of HIDDEN, freshly-injected mutants
(buggy variants of the reference) that the suite KILLS — gated by the suite passing on the
reference and on behavior-equivalent refactors (the over-specification penalty).

Non-gameable:
  - assert-False (or any suite that fails the reference) -> fails the gate -> reward 0.
  - mutants are never shown -> the agent can't target them; it must learn coverage strategy.
  - the over-spec gate kills brittle snapshot tests that depend on impl internals.

Mutants are generated by AST mutation operators (operator flips, off-by-one constants) plus
a few curated semantic bugs, then differentially filtered against the reference so the pool
contains only NON-equivalent (genuinely killable) mutants — giving a clean ceiling.
"""
from __future__ import annotations

import ast
import copy
import hashlib
import json
import random
import subprocess
import sys

# ---------------------------------------------------------------------------
# subprocess harnesses (untrusted code + our generated mutants run isolated)
# ---------------------------------------------------------------------------

_SUITE_RUNNER = r'''
import json, sys
d = json.loads(sys.stdin.read())
ns = {}
try:
    exec(d["impl"], ns)
except Exception:
    pass  # tests calling the (now-undefined) function will fail -> mutant killed
try:
    exec(d["suite"], ns)
except Exception:
    print(json.dumps({"passed": False, "n": 0})); raise SystemExit
tests = [v for k, v in list(ns.items()) if k.startswith("test_") and callable(v)]
if not tests:
    print(json.dumps({"passed": False, "n": 0})); raise SystemExit
for t in tests:
    try:
        t()
    except Exception:
        print(json.dumps({"passed": False, "n": len(tests)})); raise SystemExit
print(json.dumps({"passed": True, "n": len(tests)}))
'''

_EVAL_RUNNER = r'''
import json, sys
d = json.loads(sys.stdin.read())
ns = {}
try:
    exec(d["impl"], ns)
except Exception:
    print(json.dumps({"sig": ["IMPORT_ERR"] * len(d["inputs"])})); raise SystemExit
fn = ns.get(d["func"])
if not callable(fn):
    print(json.dumps({"sig": ["NO_FUNC"] * len(d["inputs"])})); raise SystemExit
sig = []
for args in d["inputs"]:
    try:
        v = fn(*args)
        try:
            sig.append("OK:" + json.dumps(v, sort_keys=True))
        except Exception:
            sig.append("OK:" + repr(v))
    except Exception as e:
        sig.append("ERR:" + type(e).__name__)
print(json.dumps({"sig": sig}))
'''


def _run_suite_once(impl_src: str, suite_src: str, timeout: float = 4.0) -> bool:
    """True iff the suite PASSES against this implementation."""
    payload = json.dumps({"impl": impl_src, "suite": suite_src})
    try:
        proc = subprocess.run([sys.executable, "-c", _SUITE_RUNNER], input=payload,
                              capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        return False  # a hanging impl/test => not a pass (so a hanging mutant is killed)
    out = (proc.stdout or "").strip().splitlines()
    if not out:
        return False
    try:
        return bool(json.loads(out[-1]).get("passed"))
    except Exception:
        return False


def _eval_signature(impl_src: str, func: str, inputs: list, timeout: float = 4.0) -> list:
    payload = json.dumps({"impl": impl_src, "func": func, "inputs": inputs})
    try:
        proc = subprocess.run([sys.executable, "-c", _EVAL_RUNNER], input=payload,
                              capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        return ["TIMEOUT"] * len(inputs)
    out = (proc.stdout or "").strip().splitlines()
    if not out:
        return ["EVAL_ERR"] * len(inputs)
    try:
        return json.loads(out[-1]).get("sig", ["EVAL_ERR"] * len(inputs))
    except Exception:
        return ["EVAL_ERR"] * len(inputs)


# ---------------------------------------------------------------------------
# AST mutation engine
# ---------------------------------------------------------------------------

_CMP_FLIPS = {
    ast.Lt: [ast.LtE, ast.Gt], ast.LtE: [ast.Lt, ast.GtE],
    ast.Gt: [ast.GtE, ast.Lt], ast.GtE: [ast.Gt, ast.LtE],
    ast.Eq: [ast.NotEq], ast.NotEq: [ast.Eq],
}
_BIN_FLIPS = {ast.Add: ast.Sub, ast.Sub: ast.Add, ast.Mult: ast.Add}
_BOOL_FLIPS = {ast.And: ast.Or, ast.Or: ast.And}


def _gen_ast_mutants(src: str) -> list:
    base = ast.parse(src)
    for i, n in enumerate(ast.walk(base)):
        n._mid = i
    targets = []  # (mid, kind, payload)
    for n in ast.walk(base):
        if isinstance(n, ast.Compare) and len(n.ops) == 1:
            for new in _CMP_FLIPS.get(type(n.ops[0]), []):
                targets.append((n._mid, "cmp", new))
        elif isinstance(n, ast.BinOp) and type(n.op) in _BIN_FLIPS:
            targets.append((n._mid, "bin", _BIN_FLIPS[type(n.op)]))
        elif isinstance(n, ast.BoolOp) and type(n.op) in _BOOL_FLIPS:
            targets.append((n._mid, "bool", _BOOL_FLIPS[type(n.op)]))
        elif isinstance(n, ast.Constant) and isinstance(n.value, int) and not isinstance(n.value, bool):
            targets.append((n._mid, "const", n.value + 1))
            targets.append((n._mid, "const", n.value - 1))
            if n.value not in (0, 1):
                targets.append((n._mid, "const", 0))

    out = []
    for mid, kind, payload in targets:
        t = copy.deepcopy(base)
        for n in ast.walk(t):
            if getattr(n, "_mid", None) == mid:
                if kind == "cmp":
                    n.ops = [payload()]
                elif kind in ("bin", "bool"):
                    n.op = payload()
                elif kind == "const":
                    n.value = payload
                break
        try:
            out.append(ast.unparse(ast.fix_missing_locations(t)))
        except Exception:
            pass
    return out


def _stable_seed(s: str) -> int:
    return int(hashlib.sha256(s.encode()).hexdigest(), 16) % (2**31)


